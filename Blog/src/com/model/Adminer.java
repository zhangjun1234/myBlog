package com.model;

public class Adminer {
	private int adminer_id;
	private String adminer_name;
	private String adminer_password;

	public int getAdminer_id() {
		return adminer_id;
	}

	public void setAdminer_id(int adminer_id) {
		this.adminer_id = adminer_id;
	}

	public String getAdminer_name() {
		return adminer_name;
	}

	public void setAdminer_name(String adminer_name) {
		this.adminer_name = adminer_name;
	}

	public String getAdminer_password() {
		return adminer_password;
	}

	public void setAdminer_password(String adminer_password) {
		this.adminer_password = adminer_password;
	}
}
